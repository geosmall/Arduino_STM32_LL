#ifndef UVOS_ICM42688P_CONFIG_H
#define UVOS_ICM42688P_CONFIG_H

#include "uvos_icm42688p.h"
#include "imugyroaccelsettings.h"

#define UVOS_ICM42688P_CONFIG_MAP_GYROSCALE(x) \
  (x == IMUGYROACCELSETTINGS_GYROSCALE_SCALE_250 ? UVOS_ICM42688P_SCALE_250_DEG : \
   x == IMUGYROACCELSETTINGS_GYROSCALE_SCALE_500 ? UVOS_ICM42688P_SCALE_500_DEG : \
   x == IMUGYROACCELSETTINGS_GYROSCALE_SCALE_1000 ? UVOS_ICM42688P_SCALE_1000_DEG : \
   UVOS_ICM42688P_SCALE_2000_DEG) // 2000 dps default

#define UVOS_ICM42688P_CONFIG_MAP_ACCELSCALE(x) \
  (x == IMUGYROACCELSETTINGS_ACCELSCALE_SCALE_2G ? UVOS_ICM42688P_ACCEL_2G : \
   x == IMUGYROACCELSETTINGS_ACCELSCALE_SCALE_4G ? UVOS_ICM42688P_ACCEL_4G : \
   x == IMUGYROACCELSETTINGS_ACCELSCALE_SCALE_8G ? UVOS_ICM42688P_ACCEL_8G : \
   UVOS_ICM42688P_ACCEL_16G) // 16G default

#define UVOS_ICM42688P_CONFIG_MAP_FILTERSETTING(x) \
  (x == IMUGYROACCELSETTINGS_FILTERSETTING_HIGH3 ? UVOS_ICM42688P_LOWPASS_1962_HZ : \
   x == IMUGYROACCELSETTINGS_FILTERSETTING_HIGH2 ? UVOS_ICM42688P_LOWPASS_997_HZ : \
   x == IMUGYROACCELSETTINGS_FILTERSETTING_HIGH1 ? UVOS_ICM42688P_LOWPASS_536_HZ : \
   x == IMUGYROACCELSETTINGS_FILTERSETTING_MED2 ? UVOS_ICM42688P_LOWPASS_170_HZ : \
   x == IMUGYROACCELSETTINGS_FILTERSETTING_MED1 ? UVOS_ICM42688P_LOWPASS_84_HZ : \
   x == IMUGYROACCELSETTINGS_FILTERSETTING_LOW ? UVOS_ICM42688P_LOWPASS_42_HZ : \
   UVOS_ICM42688P_LOWPASS_258_HZ) // 258Hz default

#endif /* UVOS_ICM42688P_CONFIG_H */
